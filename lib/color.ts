// Pick a readable text color for a given background hex, using relative luminance
// (WCAG). Dark ink on light backgrounds, white on dark. Falls back to dark ink
// when the input is missing or not a hex color.
export function readableTextColor(hex?: string): string {
  if (!hex) return '#0f172a';
  let h = hex.trim().replace(/^#/, '');
  if (h.length === 3) h = h.split('').map((c) => c + c).join('');
  if (!/^[0-9a-fA-F]{6}$/.test(h)) return '#0f172a';
  const r = parseInt(h.slice(0, 2), 16) / 255;
  const g = parseInt(h.slice(2, 4), 16) / 255;
  const b = parseInt(h.slice(4, 6), 16) / 255;
  const lin = (v: number) => (v <= 0.03928 ? v / 12.92 : Math.pow((v + 0.055) / 1.055, 2.4));
  const luminance = 0.2126 * lin(r) + 0.7152 * lin(g) + 0.0722 * lin(b);
  return luminance > 0.4 ? '#0f172a' : '#ffffff';
}
